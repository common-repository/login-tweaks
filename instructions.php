          <td colspan="2"><h3> Description </h3>
            <p>Instead of asking for both an email and a username on registration, this plugin lets you ask for email only, and uses that as username as well. Since passwords are automatically generated and emailed to the user, this allows for a sign-up form with a single form field: email.</p>
            <p>For users that registered with different username and email address before this plugin was installed, their username and email address will be interchangeable on login.</p>
            <h3>Usage</h3>
            <p>Once activated, the standard login and registration forms changes. The standard registration form asks only for email address, and the login form asks for 'email or username'.</p>
            <p>The plugin offers a shortcode to add the registration form to a post or page. Simply add <code>[NP-registration]</code> where you would like your registration form.</p>
            <h3>There are no settings for this plugin</h3>
